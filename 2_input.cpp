#include <iostream>
using namespace std;

int main()
{
    int a, b;
    cout << "Enter two number: ";
    // Taking multiple input at a time
    cin >> a >> b;
    // Printing multiple output at a time
    cout << "First number: " << a << endl << "Second number: " << b << endl<< "Sum of two number: " << a + b << endl;
    return 0;
}