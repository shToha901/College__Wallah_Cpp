#include<iostream>
using namespace std;
int main()
{
    int a;
    char b;
    float c;
    double d;
    bool e;
    // Multiple input at a time
    cin >> a >> b >> c  >> d  >> e;
    // Multiple output at a time
    cout << endl << a << endl << b << endl << c << endl << d << e << endl;
    return 0;
}