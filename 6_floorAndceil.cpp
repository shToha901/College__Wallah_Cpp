// CODE FOR: Floor and Ceil

#include <bits/stdc++.h>
using namespace std;

int main()
{
    // Floor
    cout << floor(3.5) << endl;
    cout << floor(4.2) << endl;
    cout << floor(6.0001) << endl;
    cout << floor(-3.5) << endl;
    cout << floor(-4.2) << endl;
    cout << floor(-6.0001) << endl << endl;

    // Ceil
    cout << ceil(3.5) << endl;
    cout << ceil(4.2) << endl;
    cout << ceil(6.0001) << endl;
    cout << ceil(-3.5) << endl;
    cout << ceil(-4.2) << endl;
    cout << ceil(-6.0001) << endl;

    return 0;
}